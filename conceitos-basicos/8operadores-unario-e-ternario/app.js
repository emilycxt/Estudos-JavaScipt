// Operador unário e operação ternaria

//UNÁRIO
let idade = 25;

idade++;//operação unária

console.log (idade);

//toda essa operação foi unaria, entende-se que operação unaria só utilizam
//um valor

//TERNÁRIO

let sexo = 'f'
if (sexo === 'm'){
    console.log ('masculino')
}else{
    console.log ('feminino')
}

//Operador ternário

console.log (sexo==='m' ? 'masculino' : 'feminino')


